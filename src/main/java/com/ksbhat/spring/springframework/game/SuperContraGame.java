package com.ksbhat.spring.springframework.game;

public class SuperContraGame implements GamingConsole {
    public void up() {
        System.out.println("Jump SuperContra");
    }
    public void down() {
        System.out.println("Go into a hole SuperContra");
    }
    public void left() {
        System.out.println("Go back SuperContra");
    }
    public void right() {
        System.out.println("Accelerate SuperContra");
    }
}
